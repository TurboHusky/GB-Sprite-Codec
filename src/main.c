enum rle_data_t
{
    RUN = 0,
    DATA = 1
};

struct bit_buffer_t
{
    uint8_t *data;
    size_t size;
    size_t byte_index;
    uint8_t bit_index;
};

void decode(const uint8_t *const data, const size_t size, const uint8_t bit_offset, uint8_t *output)
{
    (void) output;
    size_t byte_index = 0;
    int8_t bit_index = 7 - bit_offset;

    enum rle_data_t type_flag = (*data >> bit_index) & 0x01;
    bit_index = (bit_index == -1) ? 7 : bit_index - 1;

    uint64_t run_length = 0;
    uint64_t value = 0;
    uint8_t bit_count = 0;

    printf("Init: %01x - %02x\n", type_flag, *data);

    do
    {
        if (bit_index == -1)
        {
            byte_index++;
            if (byte_index >= size)
            {
                printf("ERROR: RLE read out of bounds");
                break;
            }
            bit_index = 7;
        }

        run_length <<= 1;
        printf("%u", (data[byte_index] >> bit_index) & 0x01);
        run_length |= (data[byte_index] >> bit_index) & 0x01;

        bit_count++;
        bit_index--;
    } while (run_length & 0x0000000000000001);

    printf("\n");

    for (size_t i = 0; i < bit_count; i++)
    {
        if (bit_index == -1)
        {
            byte_index++;
            if (byte_index >= size)
            {
                printf("ERROR: RLE read out of bounds");
                break;
            }
            bit_index = 7;
        }

        value <<= 1;
        printf("%u", (data[byte_index] >> bit_index) & 0x01);
        value |= (data[byte_index] >> bit_index) & 0x01;

        bit_index--;
    }
    printf("\nN = %lu\n", run_length + value + 1);
    // output N x 00 bits

    uint8_t bit_pair = 0;
    do
    {
        if (bit_index < 1)
        {
            byte_index++;
            if (byte_index >= size)
            {
                printf("ERROR: RLE read out of bounds");
                break;
            }
            bit_index += 8;
        }

        bit_pair = (bit_index == 8) ? ((data[byte_index - 1] << 1) & 0x02) | (data[byte_index] >> 7) : (data[byte_index] >> (bit_index - 1)) & 0x03;
        printf("bit pair: %u\t\t%u\n", bit_pair, bit_index);
        bit_index -= 2;
        /*
           3  2  0  3  1  2  1  3  1  0  1
         0 11 10 00 11 01 10 01 11 01 00 01 0
         71 b3 a2
        */
    } while (bit_pair);
}

